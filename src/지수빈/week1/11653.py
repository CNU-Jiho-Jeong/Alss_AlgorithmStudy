# 소인수분해
# 인수 = 곱할 때의 인자들
# 약수 = 나눌 때의 인자들
# 소수 = 약수가 1과 자기자신만인 수(1아님)
# 소인수 = 인수 중에서 소수인 것들
# 모든 수는 소인수들의 곱으로 표현 가능

# 문법
# /는 나눗셈을 의미하며 결과가 float로 나타납니다.
