# 사고
# 1. 반복이 필요한가? -> while쓰자
# 2. 반복의 멈춤 조건은 무엇인가? -> while()

'''
소수가 필요
소수의 작은 수부터 나눠본다
그 자체로 정렬됨
'''
'''
수를 2로 나눠
나머지가 0이 아니면
수를 3으로 4로 5로...

나누는걸 반복하는데
멈추는 조건은 몫이 1일 때
'''

N = int(input())
m = 2
while N!=1:  # N과m을 나눴을때 몫이 1이 되면 멈춤.
  if N%m==0: 
    print(m) 
    N = N//m
  else:
    m += 1

